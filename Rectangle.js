class Rectangle {
    length;
    width;
    constructor(length, width,area,perimeter) {
        this.length = length;
        this.width = width;
    }

    getLength() {
        return this.length;
    }

    getWidth() {
        return this.width;
    }

    getArea() {
        return this.area;
    }

    getPerimeter() {
        return this.perimeter;
    }
}
